/*
Visual how Characters are
treated like integers
*/

class CharArithmetic {
    public static void main(String args[]) {
        char ch = 'X';
        System.out.println("X is -> " + ch);

        ch++;
        System.out.println("X is -> " + ch);

        ch = 90;
        System.out.println("X is -> " + ch);
    }
}